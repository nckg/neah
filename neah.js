Neah.prototype.getRandom = function() {
    return this.quotes[Math.floor(Math.random() * this.quotes.length)];
};

Neah.prototype.paragraph = function() {
    var paragraph = [];

    for (var i = 0; i < this.quoteInParagraph; i++) {
        paragraph.push(this.getRandom());
    };

    return paragraph.join(' ');
};

Neah.prototype.paragraphs = function(count) {
    var paragraphs = [];

    for (var i = 0; i < count; i++) {
        paragraphs.push(this.paragraph());
    };

    return paragraphs.join( "\n\n" );
};

